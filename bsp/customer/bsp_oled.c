//画点 
//x:0~127
//y:0~63
void OLED_DrawPoint(INT8U x,INT8U y)
{
	INT8U i,m,n;
	i=y/8;
	m=y%8;
	n=1<<m;
	OLED_GRAM[x][i]|=n;
}


//清除一个点
//x:0~127
//y:0~63
void OLED_ClearPoint(INT8U x,INT8U y)
{
	INT8U i,m,n;
	i=y/8;
	m=y%8;
	n=1<<m;
	OLED_GRAM[x][i]=~OLED_GRAM[x][i];
	OLED_GRAM[x][i]|=n;
	OLED_GRAM[x][i]=~OLED_GRAM[x][i];
}



